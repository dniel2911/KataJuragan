public class LapakInfo {
    private String lapakName;
    private String lapakDescription;
    private String lapakLevel;
    private String lapakOpen;

    public String getLapakName() {
        return lapakName;
    }

    public void setLapakName(String lapakName) {
        this.lapakName = lapakName;
    }

    public String getLapakDescription() {
        return lapakDescription;
    }

    public void setLapakDescription(String lapakDescription) {
        this.lapakDescription = lapakDescription;
    }

    public String getLapakLevel() {
        return lapakLevel;
    }

    public void setLapakLevel(String lapakLevel) {
        this.lapakLevel = lapakLevel;
    }

    public String getLapakOpen() {
        return lapakOpen;
    }

    public void setLapakOpen(String lapakOpen) {
        this.lapakOpen = lapakOpen;
    }
}
